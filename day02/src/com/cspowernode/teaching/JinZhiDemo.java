public class JinZhiDemo {
    public static void main(String[] args) {
        //十六进制的写法：0x开头
        System.out.println(0x100);
        //十进制的写法，正常书写即可
        System.out.println(100);
        //八进制的写法，0开头
        System.out.println(0100);
        //二进制的写法，0b开头
        System.out.println(0b100);
    }
}
